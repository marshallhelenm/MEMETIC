function memeSampler() {
  let memesArray = memes.slice(0);
  let bucket = [];
  let randomIndex;

  for (let i = 0; i < 24; i++) {
    randomIndex = Math.floor(Math.random() * (memesArray.length - 1));
    bucket.push(memesArray.splice(randomIndex, 1)[0]);
  }
  return bucket;
}

